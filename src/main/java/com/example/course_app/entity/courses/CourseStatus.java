package com.example.course_app.entity.courses;

/**
 * Перечисление, представляющее возможные статусы курса.
 */
public enum CourseStatus {
    /**
     * Черновик - курс в процессе создания, не доступен студентам
     */
    DRAFT,
    
    /**
     * Опубликован - курс доступен для записи и прохождения
     */
    PUBLISHED,
    
    /**
     * Архивирован - курс больше не доступен для новых записей
     */
    ARCHIVED
}
